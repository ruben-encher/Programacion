package Ejercicios1;

public class Ejercicio_8 {
    public static void main(String[] args) {
        int N = 10;
        System.out.println("Valor inicial =" + N);
        N = N + 77;
        System.out.println("Valor incrementado =" + N);
        N = N - 3;
        System.out.println("Valor decrementado =" + N);
        N = N * 2;
        System.out.println("Valor duplicado =" + N);
    }
}