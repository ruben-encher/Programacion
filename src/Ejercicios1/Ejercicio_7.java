package Ejercicios1;

public class Ejercicio_7 {
    public static void main(String[] args) {
        int numeroX = 10;
        int numeroY = 20;
        double numeroN = 5.55;
        double numeroM = 4.44;
        double resultado = 0;

        //Suma
        resultado = numeroX + numeroY;
        System.out.println("La suma del numeroX + numeroY es = " + resultado);

        //Resta
        resultado = numeroY - numeroX;
        System.out.println("La resta del numeroY - numeroX es = " + resultado);

        //Producto
        resultado = numeroX * numeroM;
        System.out.println("El producto del numeroX * numeroM es = " + resultado);

        //Cociente
        resultado = numeroY / numeroN;
        System.out.println("El cociente del numeroY / numeroN es = " + resultado);
    }
}
